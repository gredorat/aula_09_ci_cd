def area_of_rectangle(width, height):
    area = width*height
    return area
 
def perimeter_of_rectangle(width, height):
    perimeter = 2 * (width + height)
    return perimeter

def soma(valor_1, valor_2):
    soma = valor_1 + valor_2
    return soma 

def subtracao(valor_1, valor_2):
    subtracao = valor_1 - valor_2
    return subtracao

def multiplicacao(valor_1, valor_2):
    multiplicacao = valor_1 * valor_2
    return multiplicacao

def divisao(valor_1, valor_2):
    divisao = valor_1 / valor_2
    return divisao


